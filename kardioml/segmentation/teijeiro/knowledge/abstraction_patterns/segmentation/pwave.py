def _check_histogram(hist, value):
    """
    Obtains a score of a value according to an histogram, between 0.0 and 1.0
    """
    i = 0
    while i < len(hist[1]) and value > hist[1][i]:
        i += 1
    if i == 0 or i == len(hist[1]):
        return 0.0
    else:
        return hist[0][i - 1]


